class CollegeRank {


    private String Air;
    private String Sr;

    public CollegeRank(String air, String sr) {

        Air = air;
        Sr = sr;
    }

    public String getAir() {
        return Air;
    }

    public void setAir(String air) {
        Air = air;
    }

    public String getSr() {
        return Sr;
    }

    public void setSr(String sr) {
        Sr = sr;
    }
}
